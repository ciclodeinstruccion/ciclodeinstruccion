    public void mostrar(){
        if(this.resgistrado.getPartidasJugadas()>=Registrado.getLIMITE_PARTIDAS()){
            crearPartida.setEnabled(false);
            unirsePartida.setEnabled(false);
            JOptionPane.showMessageDialog(rootPane, "Has alcanzado el n√∫mero de partidas m√°ximas permitidas. Vuelve ma√±ana para seguir jugando", "Limite alcanzado", JOptionPane.WARNING_MESSAGE);
        }
    }
