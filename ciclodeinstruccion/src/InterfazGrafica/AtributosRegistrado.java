    public void mostrar(){
        puntosDeNivel.setText(Integer.toString(registrado.getPuntosNivel()));
        vitalidad.setText(Integer.toString(registrado.getVitalidad()));
        fuerza.setText(Integer.toString(registrado.getFuerza()));
        especial.setText(Integer.toString(registrado.getEspecial()));
        inteligencia.setText(Integer.toString(registrado.getInteligencia()));
        
        if(registrado.getPuntosNivel()<=0){
            masEspecial.setEnabled(false);
            masVitalidad.setEnabled(false);
            masFuerza.setEnabled(false);
            masInteligencia.setEnabled(false);
        } 
        else{
            if(registrado.getVitalidad()>=LIMITE_HABILIDAD){
                masVitalidad.setEnabled(false);
            } else {
                masVitalidad.setEnabled(true);
            }
            if(registrado.getFuerza()>=LIMITE_HABILIDAD){
                masFuerza.setEnabled(false);
            } else {
                masFuerza.setEnabled(true);
            }
            if(registrado.getEspecial()>=LIMITE_HABILIDAD){
                masEspecial.setEnabled(false);
            } else {
                masEspecial.setEnabled(true);
            }
            if(registrado.getInteligencia()>=LIMITE_HABILIDAD){
                masInteligencia.setEnabled(false);
            } else {
                masInteligencia.setEnabled(true);
            }
        }
        
        if(registrado.getVitalidad()<=0){
            menosVitalidad.setEnabled(false);
        } else {
            menosVitalidad.setEnabled(true);
        }
        

        if(registrado.getFuerza()<=0){
            menosFuerza.setEnabled(false);
        } else {
            menosFuerza.setEnabled(true);
        }
        

        if(registrado.getEspecial()<=0){
            menosEspecial.setEnabled(false);
        } else {
            menosEspecial.setEnabled(true);
        }
        

        if(registrado.getInteligencia()<=0){
            menosInteligencia.setEnabled(false);
        } else {
            menosInteligencia.setEnabled(true);
        }
        
        
        
    }
